package com.github.fmjsjx.netty.codec.fcgi;

public class FCGIException extends Exception {

	private static final long serialVersionUID = 5229158203954203634L;

	public FCGIException() {
		super();
	}

	public FCGIException(String message, Throwable cause) {
		super(message, cause);
	}

	public FCGIException(String message) {
		super(message);
	}

	public FCGIException(Throwable cause) {
		super(cause);
	}

}
